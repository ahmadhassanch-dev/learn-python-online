# Comparison Operators
# Base values
num1 = 40
num2 = 20

# Equal to (==) , Output Type is boolean(True or false)
equal = num1 == num2
# print(f"{num1} = {num2} : {equal}" )


# Not Equal to (!=)
not_equal = num1 != num2
# print(f"{num1} is not equal to {num2} : {not_equal}")

# Greater than (>)
greater = num1 > num2
# print(f"{num1} is greater than {num2}: {greater}")

# Less than (<)
less = num1 < num2
# print(f"{num1} is less than {num2}: {less}")

#  Greater than equal to (>=)
greater_equal = num1 >= num2
# print(f"{num1} is greater than equal to {num2}: {greater_equal}")

# Less than equal to (<=)
less_equal = num1 <= num2
# print(f"{num1} is less than equal to {num2}: {less_equal}")